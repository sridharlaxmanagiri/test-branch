package com.kewill.cdr.constants;

/**
 * JasperReportFormats# interface containing report formats
 * 
 * @author rangam
 * 
 */
public interface JasperReportFormats {

    /** PDF format */
    public final String PDF = "pdf";

    /** DOCX format */
    public final String DOCX = "docx";

    /** HTML format */
    public final String HTML = "html";

    /** XLS format */
    public final String XLS = "xls";

    /** XLSX format */
    public final String XLSX = "xlsx";

    /** RTF format */
    public final String RTF = "rtf";

    /** XML format */
    public final String XML = "xml";

    /** CSV format */
    public final String CSV = "csv";

    /** PPTX format */
    public final String PPTX = "pptx";

    /** XHTML format */
    public final String XHTML = "xhtml";

    /** ODS format */
    public final String ODS = "ods";

    /** ODT format */
    public final String ODT = "odt";

}
