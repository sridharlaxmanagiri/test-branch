package com.kewill.cdr.bean;

public class CdrConfigurationBean {
	
	private String documentWebServiceURl;
	private String accessTolenURL;
	private String clientID;
	private String clientSecret;
	private String userName;
	private String password;

	/**
	 * @return the documentWebServiceURl
	 */
	public String getDocumentWebServiceURl() {
		return documentWebServiceURl;
	}
	/**
	 * @param documentWebServiceURl the documentWebServiceURl to set
	 */
	public void setDocumentWebServiceURl(String documentWebServiceURl) {
		this.documentWebServiceURl = documentWebServiceURl;
	}
	/**
	 * @return the accessTolenURL
	 */
	public String getAccessTolenURL() {
		return accessTolenURL;
	}
	/**
	 * @param accessTolenURL the accessTolenURL to set
	 */
	public void setAccessTolenURL(String accessTolenURL) {
		this.accessTolenURL = accessTolenURL;
	}
	/**
	 * @return the clientID
	 */
	public String getClientID() {
		return clientID;
	}
	/**
	 * @param clientID the clientID to set
	 */
	public void setClientID(String clientID) {
		this.clientID = clientID;
	}
	/**
	 * @return the clientSecret
	 */
	public String getClientSecret() {
		return clientSecret;
	}
	/**
	 * @param clientSecret the clientSecret to set
	 */
	public void setClientSecret(String clientSecret) {
		this.clientSecret = clientSecret;
	}
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	
}
